def using_include(arr, n)
  arr.include? n #searches the array and looks to see if n is included in it
end

def using_sort(arr)
  arr.sort #takes in the array and sorts it
end

def using_reverse(arr)
  arr.reverse #takes array and reverses the items
end

def using_first(arr)
  arr.first #takes in array and returns first item in array
end

def using_last(arr)
  arr.last #takes in array and returns last item
end

def using_size(arr)
  arr.length
end
